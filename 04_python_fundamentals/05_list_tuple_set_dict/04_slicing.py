# String slicing
name = "Anon Bianco"

print(name[0:3])

print(name[0:10:2])

# List slicing
fruits_list = ["apple", "mango", "orange", "pineapple"]
fruits_list_2 = fruits_list[0:2]
print(fruits_list_2)

# Tuple slicing
fruits_tupple = ("apple", "mango", "orange", "pineapple")
fruits_tupple_2 = fruits_tupple[0:3]
print(fruits_tupple_2)